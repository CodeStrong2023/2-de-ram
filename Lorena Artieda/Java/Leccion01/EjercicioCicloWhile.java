public class EjercicioCicloWhile {
    public static void main(String[] args) {
        var conteo= 0;
        while (conteo <3){
            System.out.println("Conteo= "+ conteo);
            conteo++;
        }
var contador= 0;
        do{
            System.out.println("Contador= "+ contador);
            contador++;
        }while (contador<=7);


    }
}

