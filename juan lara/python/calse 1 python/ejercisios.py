print("Ejercisio 1: Rango de 0 a 10 con numero divisibles entre 3")
for i in range(11):
    if i % 3 == 0:
        print(i)
        
print("Ejercisio 2: rango con valores de inicio = 2 y fin = 6")
rango = range(2, 7)
for i in rango:
    print(i)
    
print("Ejercisio 3: Crear un rango de 3 a 10, con incremento de 2 en 3")
